import random
print("a) Số nguyên ngẫu nhiên từ 0 đến 100 là: ", random.randint(0, 100))
print("a) Số thực ngẫu nhiên từ 0 đến 100 là: ", random.uniform(0, 100))

print("\nb) Số nguyên ngẫu nhiên từ 50 đến 99 là: ", random.randint(50, 99))
print("b) Số thực ngẫu nhiên từ 50 đến 99 là: ", random.uniform(50,99))

print("\nc) Số nguyên ngẫu nhiên từ -39 đến 79 là: ", random.randint(-39,79))
print("c) Số thực ngẫu nhiên từ -39 đến 79 là: ", random.uniform(-39,79))

print("\nd) Số nguyên ngẫu nhiên từ -79 đến -39 là: ", random.randint(-79,-39))
print("d) Số thực ngẫu nhiên từ -79 đến -39 là: ", random.uniform(-79,-39))